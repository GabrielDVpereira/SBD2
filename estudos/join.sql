-- INNER JOIN
---  Junçao de duas tabelas que tem pontos em comum
---  Todos os registros preenchidos dos dois lados

-- LEFT JOIN (ou LEFT OUTER)
--- Junçao de duas tablelas, mas traz dados somente da tabela da esquerda

-- RIGHT JOIN (ou RIGHT OUTER)
--- Juncao de duas tabelas, mas tras os dados da tabela a direita

-- FULL JOIN
--- Uniao entre right e left
